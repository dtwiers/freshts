export function flow(
  firstFn: (...input: unknown[]) => unknown,
  ...fns: ((input: unknown) => unknown)[]
): (...input: unknown[]) => unknown {
  return (...input: unknown[]) =>
    fns.reduce((accum, fn) => fn(accum), firstFn(...input));
}
